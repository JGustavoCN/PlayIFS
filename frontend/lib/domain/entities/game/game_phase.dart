/// Represents the different phases of a tournament.
enum GamePhase {
  /// Group stage phase.
  GROUP_STAGE,

  /// Round of 16.
  ROUND_OF_16,

  /// Quarter-finals.
  QUARTER_FINALS,

  /// Semi-finals.
  SEMI_FINALS,

  /// Third place match.
  THIRD_PLACE_DISPUTE,

  /// The final match.
  FINAL,
}